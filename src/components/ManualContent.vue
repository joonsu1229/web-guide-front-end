<script>
export default {
  name: 'ManualContent',
  props: {
    activeSection: {
      type: String,
      default: 'overview'
    },
    isDark: {
      type: Boolean,
      default: false
    }
  },
  data() {
    return {
      tocItems: [],
      activeAnchor: ''
    }
  },
  watch: {
    activeSection: {
      handler() {
        this.$nextTick(() => {
          this.extractTocItems()
          this.addScrollListener()
        })
      },
      immediate: true
    }
  },
  mounted() {
    this.extractTocItems()
    this.addScrollListener()
  },
  beforeUnmount() {
    this.removeScrollListener()
  },
  methods: {
    getBreadcrumbText() {
      const sectionMap = {
        'overview': 'ê°œìš”',
        'getting-started': 'ì‹œì‘ ê°€ì´ë“œ',
        'job-management': 'ì±„ìš©ì •ë³´ ê´€ë¦¬',
        'document-management': 'ë¬¸ì„œ ê´€ë¦¬',
        'search': 'ê²€ìƒ‰ ê¸°ëŠ¥',
        'analytics': 'ë¶„ì„ ë° í†µê³„',
        'api': 'API ì—°ë™',
        'settings': 'ì„¤ì • ê´€ë¦¬'
      }
      return sectionMap[this.activeSection] || 'ê°œìš”'
    },
    extractTocItems() {
      this.tocItems = []
      this.$nextTick(() => {
        const headings = this.$el.querySelectorAll('.content-section h3')
        headings.forEach((heading, index) => {
          const id = `heading-${index}`
          heading.id = id
          this.tocItems.push({
            id: id,
            text: heading.textContent.trim()
          })
        })
      })
    },
    scrollToSection(id) {
      const element = document.getElementById(id)
      if (element) {
        element.scrollIntoView({ behavior: 'smooth' })
      }
    },
    addScrollListener() {
      window.addEventListener('scroll', this.handleScroll)
    },
    removeScrollListener() {
      window.removeEventListener('scroll', this.handleScroll)
    },
    handleScroll() {
      if (this.tocItems.length === 0) return

      const scrollPosition = window.scrollY + 100
      let activeId = ''

      for (let i = this.tocItems.length - 1; i >= 0; i--) {
        const element = document.getElementById(this.tocItems[i].id)
        if (element && element.offsetTop <= scrollPosition) {
          activeId = this.tocItems[i].id
          break
        }
      }

      if (activeId !== this.activeAnchor) {
        this.activeAnchor = activeId
      }
    }
  }
}
</script>

